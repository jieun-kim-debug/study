# 리스트
# 변수에 여러값을 대입할 수 있는 리스트. 수치, 문자열 등의 데이터를 한꺼번에 저장할 수 있는 자료형
# error 메세지에 나왔던 오브젝트
# 리스트에 저장되어 있는 값 하나하나를 요소 또는 오브젝트라고 함

# 변수 c에 'red', 'blue', 'yellow' 세 개의 문자열을 저장
c = ['red', 'blue', 'yellow']
print(c)            # ['red', 'blue', 'yellow']

# 변수 c의 자료형 출력
print(type(c))      # <class 'list'>
