# 함수 : 처리를 정리한 프로그램
# 라이브러리, 패키지 : 여러 함수가 들어있음
# 내장함수 : 파이썬에 미리 정의된 함수 ex) print() 함수, type(), int(), str()
# 자주 사용되는 내장 함수인 len(). len() ()내 객체의 길이나 요소 수 반환
# 객체는 변수에 할당할 수 있는 요소. 대입 값은 인수(파라미터)
# len() 함수는 str형과 list형을 인수로 받을 수 있지만, int, float, bool형은 받을 수 없음

vage = "potato"
n = [4,5,2,7,6]
# 변수 vage의 객체 길이 출력
print(len(vage))        # 6
# 변수 n 객체 길이 출력
print(len(n))           # 5
